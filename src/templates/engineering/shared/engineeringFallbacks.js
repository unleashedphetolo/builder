function mergeContent(base, override) {
  return { ...base, ...(override || {}) };
}

function normalizeList(value, fallback) {
  return Array.isArray(value) && value.length ? value : fallback;
}

function normalizeContact(settings = {}, organization = {}) {
  const contact = settings.contact || {};
  return {
    email:
      contact.email ||
      settings.official_email ||
      settings.email ||
      organization.email ||
      "hello@engineeringgroup.co.za",
    phone:
      contact.phone ||
      settings.official_phone ||
      settings.phone ||
      organization.phone ||
      "+27 11 000 0000",
    address:
      contact.address ||
      settings.address ||
      organization.address ||
      "Johannesburg, South Africa",
    hours: contact.hours || settings.hours || "Mon - Fri, 08:00 - 17:00",
    mapEmbedUrl:
      contact.mapEmbedUrl ||
      contact.map_embed_url ||
      settings.mapEmbedUrl ||
      settings.map_embed_url ||
      organization.mapEmbedUrl ||
      organization.map_embed_url ||
      "https://www.google.com/maps?q=Johannesburg%2C%20South%20Africa&output=embed",
  };
}

function cleanEngineeringSiteName(value) {
  const text = String(value || "").trim();

  if (!text || text.toLowerCase() === "website preview") {
    return "";
  }

  return text;
}

function cleanEngineeringDisplayText(value) {
  const text = String(value || "").trim();
  const normalized = text.toLowerCase();

  if (!text || normalized === "website preview" || normalized === "live template preview") {
    return "";
  }

  return text;
}

export function normalizeEngineeringContent(settings = {}, preset = {}) {
  const organization = settings.organization || {};
  const contentKey = preset.contentKey || "base";
  const templateContent = mergeContent(BASE_ENGINEERING_FALLBACKS, ENGINEERING_TEMPLATE_CONTENT[contentKey]);
  const savedContent =
    settings.engineering_content ||
    settings.engineeringContent ||
    settings.template_content?.engineering ||
    settings.templateContent?.engineering ||
    {};

  const merged = mergeContent(templateContent, savedContent);

  return {
    ...merged,
    businessName:
      cleanEngineeringSiteName(settings.site_name) ||
      cleanEngineeringSiteName(settings.website_name) ||
      cleanEngineeringSiteName(organization.name) ||
      merged.businessName,
    tagline: cleanEngineeringDisplayText(settings.tagline) || cleanEngineeringDisplayText(merged.tagline) || templateContent.tagline,
    motto: settings.motto || merged.motto,
    services: normalizeList(savedContent.services, merged.services),
    solutions: normalizeList(savedContent.solutions, merged.solutions),
    industries: normalizeList(savedContent.industries, merged.industries),
    features: normalizeList(savedContent.features, merged.features),
    packages: normalizeList(savedContent.packages, merged.packages),
    stats: normalizeList(savedContent.stats, merged.stats),
    process: normalizeList(savedContent.process, merged.process),
    testimonials: normalizeList(savedContent.testimonials, merged.testimonials),
    values: normalizeList(savedContent.values, merged.values),
    contact: normalizeContact(settings, organization),
    socialLinks:
      settings.social_links ||
      settings.socialLinks ||
      organization.social_links ||
      {},
    socialDisplay:
      settings.social_display ||
      settings.socialDisplay ||
      organization.social_display ||
      {},
  };
}
